	<p class="italic">
		Früher war die angesehene Schweizer Bankiersfamilie Prinn ein festes Standbein der feinen
		Gesellschaft. Um ihre Dinner-Parties und Feste rankten sich einst Legenden, und namhafte
		Gestalten aus Politik, Kunst und Wissenschaft gingen in ihrer Stadtwohnung in Basel ein und aus.
		In den letzten zwanzig Jahren ist es allerdings still geworden um das Ehepaar. Gerüchte kamen
		auf und verklangen wieder, bis ihr Name zu einer Randnotiz der Gesellschaft verklang. Doch dann
		wird euch ein unerwarteter Brief zugestellt ...
	</p>

	<p class="mt-4 text-center font-bold">
		Äthertanz I: Höhenluft<br />
		Eine Mythos-Convention des Nächtliches Theater e.V.<br />
		03.-05. Oktober 2025<br />
		Pfadfinder Centrum Callenberg
	</p>

	<p class="mt-4">
		<span class="font-bold">Erlebt eine düstere Soirée, inspiriert vom Cthulhu-Mythos!</span><br />
		Für dieses völlig neue Projekt richten wir in den historischen Stallungen des Schlosses Callenberg
		und dem umliegenden Wald ein abgelegenes Jagdhaus in den Schweizer Alpen her. Eine zurückgezogen
		lebende Bankiersfamilie lädt zu einer Zusammenkunft schillernder Gestalten der Gesellschaft ein -
		doch jeder, der kommt, hat seine Geheimnisse und seine eigene dunkle Vergangenheit, und nach und
		nach wird deutlich, dass hier etwas ganz und gar nicht stimmen kann …
	</p>

	<p>
		Es handelt sich hierbei um einen 1,5-Tages-Con im Chtulhu-Setting der 1920er. Der Spielstil soll
		hierbei von schleichendem psychologischen Horror und Grusel geprägt sein. Auch Krimielemente
		wird es auf jeden Fall geben. Wir spielen im Cthulhu-Mythos, aber auch in einem
		gesellschaftlichen Setting. Das heißt: Es wird nicht immer zur vollen Stunde ein Tentakelmonster
		auftauchen, das alle gemeinsam blutig abschlachten - vielmehr werdet ihr feststellen, wie der
		Wahnsinn nach und nach seinen Weg in die zu Beginn friedliche Zusammenkunft bahnt. Es geht um
		unterschwelligen Horror in einem dekadent-düsteren Milieu. Potenziell kann es zu verstörenden
		Szenen kommen, etwa der Darstellung von Gewalt, Drogenkonsum, Trauma und ähnlichem. Wir haben
		euch gewarnt.
	</p>
	<p>
		Plot, Rollen und Räumlichkeiten werden detailliert ausgearbeitet, um euch ein immersives
		Erlebnis der ganz besonderen Art zu bieten. Wir begrüßen euch in den Zwanziger Jahren fern von
		den Zwängen der Gesellschaft zum Äthertanz!
	</p>

	<h3 class="h3 mt-4">Hier die Details</h3>
	<p>
		<span class="font-bold">Rollen:</span> Eure Charaktere sind gesellschaftlich interessante Figuren,
		gerne angelehnt an die klassischen Professionen aus Lovecraft Werken oder dem Pen and Paper Call
		of Cthulhu: Künstler, Ermittler, Archäologen, Seefahrer, Geistliche, Wissenschaftler, ... Jeder Charakter
		sollte zwei Dinge mitbringen: Erstens legt jeder ein persönliches Trauma oder eine düstere Seite
		fest, die ihn empfänglich macht für die groteske Szenerie. Wir treten bezüglich des Hintergrundes
		im Laufe der Anmeldungsphase mit euch in den Dialog. Zweitens sollte jeder Charakter etwas beisteuern,
		um anderen Charakteren Spiel zu bieten. Bei diesem gesellschaftlichen Anlass kann z.B. ein Autor
		ein Gedicht vortragen oder verzweifelt nach Inspiration suchen, eine schwarze Witwe Jagd auf einen
		neuen Ehemann machen oder ein Spiritualist eine Seance abhalten – Hauptsache, es sorgt für Furore.
		Spielercharaktere haben keine Waffen, keine Magie und kein (korrektes) übernatürliches Wissen.
	</p>

	<p>
		<span class="font-bold">Regelwerk:</span> Die Veranstaltung ist eine klassische Larp-Veranstaltung,
		die dem Prinzip "Du kannst, was du (schön) darstellen kannst” folgt. Einen Charakterbogen gibt es
		also nicht - alles wird ausgespielt. Kämpfe und Verwundungen sind schnell tödlich, wobei eine gesellschaftliche
		Soirée ohnehin kein Ort für Prügeleien sein sollte. Rückfragen bei der Spielleitung zu Wissenschaften,
		Fremdsprachen und ähnlichem sind nicht möglich. Nutzt, was ihr könnt, bereitet euch vor - und stellt
		es entsprechend dar!
	</p>

	<p>
		<span class="font-bold">Kost und Logis:</span> IT befinden wir uns im Herbst 1925 in einem
		abgelegenen Dorf in den Schweizer Alpen – OT geht es in das
		<a href="https://pfadfinder-abenteuer.de/unterkuenfte/pfadfinder-centrum-callenberg/">
			Pfadfinder Centrum Callenberg
		</a> bei Coburg. Die Unterbringung erfolgt in Gemeinschaftsschlafsälen. Bettzeug (inkl. Decken und
		Kissen) muss selbst mitgebracht werden. Die Schlafräume dienen als Rückzugsort, sonst wird dauerhaft
		Spiel betrieben - rund um die Uhr. Für OT-Blasen ist hier kein Platz, ebenso wenig wie für übermäßigen
		Alkoholgenuss. Dies ist keine Feier-Con! Beiträge für Bar, Kuchenbuffet und Snacktisch sind gern
		gesehen. Hier ist stets darauf zu achten, zumindest ansatzweise dem Setting Respekt zu zollen - Marken
		passen hier wenig, genau wie allzu offensichtlich moderne Speisen und Getränke (z.B. Energydrinks).
	</p>
	<p>
		<span class="font-bold">Preis:</span> Wir bieten euch für 100 Euro zwei Übernachtungen, drei Mahlzeiten
		pro Tag sowie Snacks und nichtalkoholische Getränke. Alkoholische Getränke können selbst mitgebracht
		oder im Rahmen der Verfügbarkeit zu günstigen Preisen an der Bar bestellt werden. Der Geldeingang
		muss bis zum 01.07.2025 erfolgen.
	</p>
	<p>
		<span class="font-bold">Zeit:</span> 03.-10. Oktober 2025. Wir bespielen IT die Anreise, insofern
		bitten wir OT um Pünktlichkeit. Die OT-Anreise ist ab 18 Uhr möglich, um 19 Uhr wollen wir IT gehen.
		Wer nicht pünktlich anreisen kann, möge uns bitte Bescheid geben. Das Intime endet in der Nacht von
		Samstag auf Sonntag. Der Barbetrieb bleibt danach noch bestehen, und am Folgetag können wir gemeinsam
		beim Frühstück Revue passieren lassen.
	</p>
	<p>
		<span class="font-bold">Kleidung:</span> Achtet auf angemessene Garderobe. Wir erheben keinen Anspruch
		auf historische Korrektheit, aber die Gewandung sollte zumindest zum Setting passen.
	</p>
	<p>
		<span class="font-bold">Anreise:</span> Für Anreisende mit dem PKW stehen ausreichend Parkplätze
		direkt vor der Location zur Verfügung. Bei Anreise mit öffentlichen Verkehrsmitteln halten die Buslinien
		1405, 1465 und 1427 in Fußlaufreichweite entfernt.
	</p>
	<p>
		<span class="font-bold">Inspiration:</span> Wir halten alle Teilnehmer dazu an, sich im Vorfeld grob
		mit den Gepflogenheiten der Zeit, des Settings und ihrer Profession vertraut zu machen. Die Medienlandschaft
		bietet aktuell viele Möglichkeiten, um beispielsweise den Stand der Medizin in den Zwanzigern nachzuvollziehen.
	</p>

